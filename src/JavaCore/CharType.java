package JavaCore;

public class CharType {

    public static void main(String[] args) {

        char c = 'A';
        System.out.println(c);

        int i = c;
        System.out.println(i);

        char c1 = 'A' + 2;
        System.out.println(c1);

        int i1 = c1;
        System.out.println(i1);
    }
}
