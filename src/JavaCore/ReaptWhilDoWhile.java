package JavaCore;

public class ReaptWhilDoWhile {

    public static void main(String[] args) {

        int i = -5;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("FIM");


        int j = 0;

        do {
            System.out.println(j);
            j++;
        } while (j <= 10);
    }
}
